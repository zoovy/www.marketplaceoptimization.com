<h1>Marketplace Optimization&trade;</h1>
<h3>[m&#228;r-k&#601; t-,pl&#257;s\&#228;p-t&#601; -m&#601; -'z&#257;-sh&#601; n ] adjective:</h3>
<p>the sales position (ranking) of a company on one or more marketplaces.</p>
<p>
Marketplace Optimization&trade; studies and adjusts data to best match the ranking algorithms 
(signals) used by marketplaces such as Amazon, eBay, Buy.com, etc. to select preferred vendors.  
By correctly using Marketplace Optimization&trade; to identify and improve signal performance 
a company will receive preference, resulting in increased exposure (sales) on a marketplace. 
</p>
<p>Marketplace Optimization starts by conducting a comprehensive review to identify poorly performing signals
based on priority, and then recommends corrective actions to increase signal performance and will create additional 
marketplace referred sales.
</p>
<p>
Marketplace Optimization can also be employed as a preventative measure to identify opportunities competitors may have
to steal marketplace referred sales.
</p>
</div>

<h2 id='fiveKeyComponents'>The 5 Essentials of Marketplace Optimization&trade;</h2>
<div id='accordion'>
	<h3><a href="#section1">Minimize Order Cancellations</a></h3>
	<div>
<p>The leading cause for order cancellations is due to incorrect inventory.  Sellers need to gain more control and display accurate inventory to prevent accidental overselling.  Sellers that advertise products across multiple marketplaces/channels or utilize multiple warehouses and do not have automation software in place to synchronize inventory quickly will have inaccurate inventory leading to an increase of overselling.  Human error is also a risk for cause of order cancellations, implementing shipping software that will track employee shipping errors can be an indispensable tool in lowering order cancellation rates.  In the event where overselling occurs having a backup vendor in place to fulfill an order is key when inventory is not available.</p>
	</div>
	<h3><a href="#section2">Increase Inventory Availability</a></h3>
	<div><p>One of the signals marketplaces use for determining which seller to display for a particular product is the amount of inventory available for purchase.  Therefore if inventory is currently being arbitraged across multiple marketplaces, sending up all inventory will result in a better sell through.
If inventory is low the automation software needs to be allocating (reserving) to avoid cancellations.  
There are infinite possible strategies when determining optimal allocation when limited quantities of inventory are available to distribute across multiple marketplaces.
</p></div>
	<h3><a href="#section3">Perform Dynamic Repricing</a></h3>
	<div>
<p>Often times price is the only competitive advantage sellers have. Repricing is defined as the action of going to a marketplace to see other sellers offering the same item for sale and either matching or beating the sale price, the shipping costs, or both. Repricing can be instrumented automatically by means of a software or manually.</p>
<p>There are nearly infinite strategies available for repricing, for example a seller with a low cancellation rate will only need to match the best price in order to win the "Buy Box".  
Repricing systems which take into account customer feedback, number of items on order, or overall margins and sales velocity across all marketplace channels to make more intelligent repricing decisions and maximize operating revenues are key.  </p>
	</div>
	<h3><a href="#section4">Improve Customer Experience</a></h3>
	<div>
<p>Positive customer feedback is vital for seller success.  Sellers should be requesting feedback when necessary from customers via email, it is more convenient for a customer to leave feedback when the information is provided electronically.  This request can be done by means of an integrated CRM tool or manually.  With each marketplace having its own guidelines it is vital that if utilizing a CRM tool it be flexible to accommodate differences.</p>
<p>If and when negative issues arise sellers should be proactive and resolve the issues as quickly as possible.  Once the issue has been resolved it is in most cases permissible to request the feedback score be updated.</p>
	</div>
	<h3><a href="#section5">Logistics Optimization</a></h3>
	<div>
<p>Sellers need to identify weaknesses and make business decisions that will increase performance and sales.  Consistent late shipments and expensive shipping costs are areas to consider improving when considering seller performance.</p>
<p>Order management software can streamline operations and reduce shipping latency (a measurement of the amount of time after an order is placed until it ships).</p>
<p>Sellers need to identify when it makes the most sense to utilize a fulfillment center to cut down on shipping costs and lower shipment times.</p>
<p>Utilizing multiple warehouses and /or fulfillment centers in different geographic locations can result in a cost savings on expedited shipping simply because these warehouses put items closer to customers as opposed to being located in one location on one side of the coast.  This allows for more competitive shipping and/or expedited shipping costs.</p>
	</div>
</div>
<h2 id='theBuyBox'>Competing for the "Buy Box"</h2>
<p> When multiple sellers offer the same item - each marketplace uses proprietary  algorithms to determine which seller or sellers it recommends to the buyer.    This area on the marketplace where the recommended sellers are displayed to a prospective buyer is often termed the "Buy Box". </p>
