<h2 id='differencesToSeo'>How is Marketplace Optimization&trade; Different than Search Engine Optimization?</h2>
<p>
Search Engine Optimization is a series of techniques used to improve the website position in a SERP (Search Engine Result Page).
SERPs are influenced by signals such as meta tag content, link count/quality, keyword density, domain name age and other arcane factors
that are typically good indicators of what a page is about, but very poor indicators for determining if the website offering 
the content (product) is reliable, or offers the best consumer experience.
</p>
<p>
Marketplace Optimization&trade; is not concerned with the search result position of a product because the description of an item,
its keywords, and ultimately its sales rank is controlled/managed by the marketplace and influenced by buyers rankings of the item.  
Marketplace Optimization&trade; is only concerned with influencing the position of a seller in relation to its competitors on a specific product listing.
</p>

