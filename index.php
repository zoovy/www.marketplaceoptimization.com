<h2 id='rankingSignals'>What are Marketplace Ranking Signals?<span class='super'>*</span></h2>
<ul class='arrowList'>
	<li>Total cost to buyer (including shipping)</li>
	<li>Inventory quantity listed as available by the seller</li>
	<li>Overall order cancellation rate of the seller</li>
	<li>Specific order cancellation rate of the seller for the product.</li>
	<li>Availability of expedited shipping</li>
	<li>Overall customer feedback / reviews</li>
	<li>The 7, 30, 90 day and lifetime seller performance</li>
	<li>Gross sales of the seller</li>
	<li>Order defect rate as a % of gross sales</li>
	<li>Return policy</li>
</ul>
<p id='marketplaceRankingAsterisk'>* signals are listed as examples, signals vary dependent upon marketplace</p>


